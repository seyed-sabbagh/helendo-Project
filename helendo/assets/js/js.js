function getImageDetail1() {
    // Get the color of the button on the first page
    const imageAddress = document.getElementById('ImageN1').src;
    const productContent = document.getElementById('product-content-1').text;
    localStorage.setItem('productContent', productContent);

    localStorage.setItem('imageAddress', imageAddress);


    // Redirect to the second page
    window.location.href = 'product-details-two.html';

    console.log(imageAddress);

}

